package com.psl.bean;

public class Address {
	
	private String street,city,zip;
	public Address() {
		System.out.println("IN addr >>default constructor");

	}
	public Address(String street, String city, String zip) {
		super();
		System.out.println("IN addr >>para constructor");
		this.street = street;
		this.city = city;
		this.zip = zip;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		System.out.println("IN addr >> SetStreet");
		this.street = street;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		System.out.println("IN addr >> SetCity");
		this.city = city;
	}
	public String getZip() {
		return zip;
	}
	public void setZip(String zip) {
		System.out.println("IN addr >> setZip");
		this.zip = zip;
	}
	@Override
	public String toString() {
		return "Address [street=" + street + ", city=" + city + ", zip=" + zip
				+ "]";
	}
	
	

}
